package study0503;

public class Method_test2 {

	
		public static void loop10() {
			for(int i=1; i<=10; i++) {
				System.out.print(i+" ");
			}
		}
		public static void loop20() {
			for(int i=1;i<=20;i++) {
				System.out.print(i+" ");
			}
		}
		
		
		
	public static void main(String[] args) {
		loop10();
		System.out.println();
		loop20();

	}

}
